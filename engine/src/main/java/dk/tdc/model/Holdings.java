package dk.tdc.model;

public class Holdings {
    private double price;
    private double pieces;

    public double getPrice() {
        return price;
    }

    public double getPieces() {
        return pieces;
    }

    public void setPieces(double pieces) {
        this.pieces = pieces;
    }

    public void setPrice(double price) {
        this.price = price;
    }
}
